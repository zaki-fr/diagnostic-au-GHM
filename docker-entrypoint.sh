#!/bin/bash

set -e

if [ "$1" = 'npm' ]; then
    echo "Running npm option."
    # Some app use this key for a consistency 
    # encryption purpose then need to be fixed.
    # DONOT uncomment this line for any purpose.
    exec /usr/bin/gosu "$(getent passwd "${USER_ID}" | cut -d: -f1)" "$@"
fi
# As argument is not related to node,
# then assume that user wants to run his own process,
# for example a `bash` shell to explore this image
exec "$@"